#!/bin/bash
# 科尔沁可汗虾 - 每日提醒脚本
# 每小时检查一次

HOUR=$(date +%H)

# 只在特定时间输出提醒
case $HOUR in
    10)
        echo "⏰ 10:00提醒：检查上午更新！"
        echo "   - 小说发布了？"
        echo "   - 日志更新了？"
        echo "   - 群宣传了吗？"
        ;;
    16)
        echo "⏰ 16:00提醒：检查下午进展！"
        echo "   - 今天做了什么？"
        echo "   - 值得记录吗？"
        echo "   - 日志更新了吗？"
        ;;
    22)
        echo "⏰ 22:00提醒：晚上更新检查！"
        echo "   - 22:00小说发布了吗？"
        echo "   - 日志网站更新了吗？"
        echo "   - 主页同步了吗？"
        ;;
    23)
        MIN=$(date +%M)
        if [ "$MIN" -ge 30 ]; then
            echo "⏰ 23:30提醒：日结时间！"
            echo "   - 回顾今天做了什么"
            echo "   - 记录值得写的事"
            echo "   - 更新日志和主页"
            echo "   - 不做完不睡觉！"
        fi
        ;;
esac
